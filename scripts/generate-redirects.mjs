// Generates Netlify redirect rules from the redirects.yml files that ship
// with the versioned docs (versioned_docs/version-*/redirects.yml).
//
// The unversioned /docs/* URL space serves the latest docs version, so when a
// page moves, its old unversioned URL must redirect. Each docs version
// declares its moves in redirects.yml; this script unions them across all
// versions (later versions win), resolves redirect chains, and rewrites the
// marked section of static/_redirects. Everything outside the markers is
// hand-maintained and left untouched.
//
// Run via `make generate-redirects` (also invoked by generate-version).
import fs from "node:fs";
import path from "node:path";
import YAML from "yaml";

const BEGIN = "# BEGIN docs redirects generated by scripts/generate-redirects.mjs - do not edit";
const END = "# END docs redirects";

const versionedDocs = "versioned_docs";
const redirectsFile = path.join("static", "_redirects");

const versions = fs
	.readdirSync(versionedDocs)
	.filter((d) => d.startsWith("version-"))
	.map((d) => ({ dir: d, ver: d.substring("version-".length).split(".").map(Number) }))
	.sort((a, b) => a.ver[0] - b.ver[0] || a.ver[1] - b.ver[1]);

const rules = new Map();
for (const { dir } of versions) {
	const file = path.join(versionedDocs, dir, "redirects.yml");
	if (!fs.existsSync(file)) {
		continue;
	}
	for (const entry of YAML.parse(fs.readFileSync(file, "utf8")) ?? []) {
		const { from, to } = entry;
		if (from && to && from !== to) {
			rules.set(from, to);
		}
	}
}

// Resolve chains: a -> b and b -> c becomes a -> c.
for (const [from] of rules) {
	let to = rules.get(from);
	const seen = new Set([from]);
	while (rules.has(to) && !seen.has(to)) {
		seen.add(to);
		to = rules.get(to);
	}
	rules.set(from, to);
}

const block = [BEGIN];
for (const from of [...rules.keys()].sort()) {
	block.push(`/docs/${from} /docs/${rules.get(from)} 301`);
}
block.push(END);

let content = fs.readFileSync(redirectsFile, "utf8");
const beginAt = content.indexOf(BEGIN);
if (beginAt >= 0) {
	const endAt = content.indexOf(END, beginAt);
	if (endAt < 0) {
		throw new Error(`${redirectsFile}: found the BEGIN marker but not the END marker`);
	}
	content = content.substring(0, beginAt) + block.join("\n") + content.substring(endAt + END.length);
} else {
	content = content.trimEnd() + "\n\n" + block.join("\n") + "\n";
}
fs.writeFileSync(redirectsFile, content);
console.log(`${redirectsFile}: wrote ${rules.size} generated docs redirects`);
